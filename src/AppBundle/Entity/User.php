     * @Assert\NotBlank(message="Last name cannot be blank")
     */
    private $lastName;

    /**
     * @var Countries
     * @ORM\ManyToOne(targetEntity="AppBundle\Entity\Countries")
     * @ORM\JoinColumn(name="country_id", referencedColumnName="id")
     */
    private $country;

